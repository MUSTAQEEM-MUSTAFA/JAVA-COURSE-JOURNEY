package section01;

public class VariablesAndDataTypes {
    public static void main(String[] args) {
        int myAge = 18;
        double myHeight = 5.9;
        final int TOTAL_MONTHS = 12;
        String myName = "Mustaqeem Mustafa";
        String myHomeTown = "Hyderabad, Pakistan";
        System.out.println("My name is " + myName);
        System.out.println("My height is " + myHeight + " feet.");
        System.out.println("I am " + myAge + " years old.");
        System.out.println("There are " + TOTAL_MONTHS + " months in a year.");
        System.out.println("I live in " + myHomeTown);
    }
}
